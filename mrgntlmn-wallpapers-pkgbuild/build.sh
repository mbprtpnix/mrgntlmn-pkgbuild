#!/bin/bash
#set -e -u

CHROOT=$HOME/chroot

tput setaf 2
echo "#########################################################################"
echo "###################### UPDATING CHROOT ENVIRONMENT ######################"
echo "#########################################################################"
tput sgr0

arch-nspawn $CHROOT/root pacman -Syu

tput setaf 2
echo "#########################################################################"
echo "############################ STARTED MAKEPKG ############################"
echo "#########################################################################"
tput sgr0

makechrootpkg -c -r $CHROOT -- --syncdeps --check

tput sgr0
echo "#########################################################################"
echo "################################# DONE ##################################"
echo "#########################################################################"
tput sgr0
